# Simple Python code snippet that imports two modules and uses them:

import math, random

# Using the math module to calculate the square root of a number
number = 16
sqrt_number = math.sqrt(number)
print(f"The square root of {number} is {sqrt_number}")

# Using the random module to generate a random number between 1 and 10
random_number = random.randint(1, 10)
print(f"A random number between 1 and 10 is {random_number}")