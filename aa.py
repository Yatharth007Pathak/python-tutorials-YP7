# Programming is the process of creating a set of instructions (called programs) that tells a computer how to perform a task


'''
Programming is the process of designing and building an executable computer program to accomplish a specific computing task. 
It involves writing code in a programming language to create software applications, scripts, 
or other sets of instructions that a computer can execute. Programming encompasses several key activities:

Problem Analysis: Understanding the problem that needs to be solved and determining the requirements.
Algorithm Design: Creating a step-by-step plan or set of rules to solve the problem.
Coding: Writing the actual code in a programming language (e.g., Python, Java, C++) based on the designed algorithm.
Testing: Running the program to find and fix errors or bugs.
Debugging: Identifying and resolving issues in the code.
Documentation: Writing explanations and comments in the code to help others understand its purpose and functionality.
Maintenance: Updating and improving the program over time to adapt to new requirements or correct issues.

Programming is fundamental to software development and is used to create everything from simple scripts to complex applications and systems.
'''

"""
Source code refers to the human-readable instructions written in a programming language. 
It is the original form of code created by a programmer to implement specific tasks, algorithms, or functionalities. 
Source code is written in high-level programming languages like 
Python, Java, C++, or JavaScript, which are designed to be readable and understandable by humans

Machine code, also known as machine language, is the lowest-level representation of code that a computer's central processing unit (CPU) 
can directly execute. It consists of binary digits (0s and 1s) and is specific to a particular type of processor. 
Machine code is not human-readable and is the result of compiling or assembling source code. 
The translation from source code to machine code is performed by a compiler or an assembler.

Source Code: Written by programmers in high-level, human-readable languages.
Machine Code: The binary instructions that a computer's CPU executes, generated from the source code by compilers or assemblers.
"""

'''
Bytecode is an intermediate representation of a program, which lies between the high-level source code and the machine code 
that is executed by the hardware. It is typically generated by compilers and interpreted or executed by virtual machines. 
It is more abstract than machine code and is used in the implementation of some programming languages. 
In the context of Python, bytecode is the result of compiling Python source code (.py files) into an intermediate form (.pyc or .pyo files). 
This bytecode is then executed by the Python interpreter's virtual machine.

Examples:

Java Bytecode: Compiled from Java source code, executed by the JVM.
Python Bytecode: Compiled from Python source code, executed by the Python interpreter.
.NET Intermediate Language (IL): Compiled from languages like C# or VB.NET, executed by the .NET Common Language Runtime (CLR).
'''

