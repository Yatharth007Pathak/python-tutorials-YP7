x = 5
y = "2"

print(x + int(y))
print(str(x) + y)

'''
Addition of x and int(y):
x is an integer with a value of 5.
y is a string with a value of "2".
int(y) converts the string "2" to the integer 2.
Adding x and int(y) results in 5 + 2, which is 7.
'''

'''
Concatenation of str(x) and y:
str(x) converts the integer 5 to the string "5".
y is "2", so concatenating str(x) and y results in "5" + "2", which is "52"
'''